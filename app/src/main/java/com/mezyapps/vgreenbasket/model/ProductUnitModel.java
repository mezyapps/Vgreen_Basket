package com.mezyapps.vgreenbasket.model;

public class ProductUnitModel {
    private String id;
    private String prod_id;
    private String prod_unit;
    private String prod_rate;
    private String prod_weight;
    private String prod_weight_id;
    private String prod_mrp;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getProd_id() {
        return prod_id;
    }

    public void setProd_id(String prod_id) {
        this.prod_id = prod_id;
    }

    public String getProd_unit() {
        return prod_unit;
    }

    public void setProd_unit(String prod_unit) {
        this.prod_unit = prod_unit;
    }

    public String getProd_rate() {
        return prod_rate;
    }

    public void setProd_rate(String prod_rate) {
        this.prod_rate = prod_rate;
    }

    public String getProd_weight() {
        return prod_weight;
    }

    public void setProd_weight(String prod_weight) {
        this.prod_weight = prod_weight;
    }

    public String getProd_weight_id() {
        return prod_weight_id;
    }

    public void setProd_weight_id(String prod_weight_id) {
        this.prod_weight_id = prod_weight_id;
    }

    public String getProd_mrp() {
        return prod_mrp;
    }

    public void setProd_mrp(String prod_mrp) {
        this.prod_mrp = prod_mrp;
    }
}
